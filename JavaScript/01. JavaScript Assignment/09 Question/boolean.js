// Truthy Values

let string = "Hello, world!";
let number = 75;
let object = {};
console.log(`A non empty string = "${string}" is truthy value`); 
console.log(`A non-zero number = ${number} is truthy value`);
console.log(`An object = ${object} is truthy value`);


// Falsy Values

let stringEmpty = "";
let numberZero = 0;
let undefined;
console.log(`A empty string = "${stringEmpty}" is falsy value`); 
console.log(`A zero number = ${numberZero} is falsy value`);
console.log(`${undefined} is falsy value`);