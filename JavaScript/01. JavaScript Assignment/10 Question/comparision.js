// The result of 4 > 3 is true.
console.log(4 > 3);

// The result of 4 >= 3 is true.
console.log(4 >= 3);

// The result of 4 < 3 is false.
console.log(4 < 3);

// The result of 4 <= 3 is false.
console.log(4 <= 3);

// The result of 4 == 4 is true.
console.log(4 == 4);

// The result of 4 === 4 is true.
console.log(4 === 4);

// The result of 4 != 4 is false.
console.log(4 != 4);

// The result of 4 !== 4 is false.
console.log(4 !== 4);

// The result of 4 != '4' is false.
console.log(4 != '4');

// The result of 4 == '4' is true.
console.log(4 == '4');

// The result of 4 === '4' is false.
console.log(4 === '4');

